set define off;

  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "PROYECTO"."CALCULAR_TOTAL_FACTURA" (id_factura number)
is
valor_reserva number;
valor_detalles number;
total number;
begin
    valor_reserva := fun_total_reserva_por_factura(id_factura);
    valor_detalles := fun_total_factura_por_detalles(id_factura);

    total := valor_reserva + valor_detalles;

    UPDATE facturas set totalcuenta = total WHERE idfactura = id_factura;
end;

/
--------------------------------------------------------
--  DDL for Function FUN_TOTAL_FACTURA_POR_DETALLES
--------------------------------------------------------

  CREATE OR REPLACE NONEDITIONABLE FUNCTION "PROYECTO"."FUN_TOTAL_FACTURA_POR_DETALLES" (id_factura number)
return number
is 
total number := 0;
CURSOR cur_detalles is 
SELECT valor from factura_detalles 
WHERE idfactura = id_factura;
begin

 for fila IN cur_detalles loop
    total := total + fila.valor;
 end loop;
 return total;
end;

/
--------------------------------------------------------
--  DDL for Function FUN_TOTAL_RESERVA_POR_FACTURA
--------------------------------------------------------

  CREATE OR REPLACE NONEDITIONABLE FUNCTION "PROYECTO"."FUN_TOTAL_RESERVA_POR_FACTURA" (id_factura number)
return number
is 
total number := 0;
CURSOR cur_valor_tipo_habitacion is 
SELECT th.precio from reservaciones r 
INNER JOIN facturas f ON f.numeroreserva = r.numeroreserva
INNER JOIN habitaciones h ON r.idhabitacion = h.idhabitacion
INNER JOIN tipo_habitaciones th ON th.idtipohabitacion = h.idtipohabitacion
WHERE f.idfactura = id_factura;
begin

 for fila IN cur_valor_tipo_habitacion loop
    total := fila.precio;
 end loop;
 return total;
end;

